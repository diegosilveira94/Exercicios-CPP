// 2. Crie uma função que receba dois números inteiros e retorne o maior deles.
int verificarNumMaior(int num1, int num2)
{
	int maior = 0;
	if (num1 > num2)
		maior = num1;
	else
		maior = num2;
	return (maior);
}
